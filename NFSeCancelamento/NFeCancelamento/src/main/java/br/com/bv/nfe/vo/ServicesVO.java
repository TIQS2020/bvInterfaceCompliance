package br.com.bv.nfe.vo;

public class ServicesVO {

	private String cancelamentoServiceURL;
	private String consultaServiceURL;
	
	public ServicesVO(){}

	public String getCancelamentoServiceURL() {
		return cancelamentoServiceURL;
	}

	public void setCancelamentoServiceURL(String cancelamentoServiceURL) {
		this.cancelamentoServiceURL = cancelamentoServiceURL;
	}

	public String getConsultaServiceURL() {
		return consultaServiceURL;
	}

	public void setConsultaServiceURL(String consultaServiceURL) {
		this.consultaServiceURL = consultaServiceURL;
	}
	
	

	
	
}
